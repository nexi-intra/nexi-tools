title: EY - Proposed Use Cases
---
The application reachable for all users where they can search for corporate tools, both from the Group and from their Country. To do this,
here a list of requirements that the webpart should satisfy:
• embedded on a page of the Intranet and available on Teams
• all tools (Group + Countries) are collected in one list
• search box with filters
• user sees targeted tools → Group + his Country
• the admin can set up an initial list of favourite tools, which the user can later remove from favorites.
• user can set preferred tools
• preferred tools are in evidence
• each tool belongs to a category or tag (a category list must be previously defined)
• user can browse or search tools out of his target
• multilanguage available (English + Italian)
• responsive


